def f(a, b):
    # Базовый случай: любая степень с показателем 0 равна 1
    if b == 0:
        return 1
    # Если показатель степени отрицательный, возводим в положительную степень и берём обратное значение
    elif b < 0:
        return 1 / f(a, -b)
    # Рекурсивный случай: возводим в степень, уменьшая показатель на 1
    else:
        return a * f(a, b - 1)

# Примеры
print(f(3, 5))  # 243
print(f(2, 3))  # 8
